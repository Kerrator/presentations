#!/usr/bin/env python3
"""HTST prefactor "catch" figure for "Coding with Claude Code".

The case-study payoff: the on-lattice catalogue's HTST build (pylatkmc/ingest/htst:
canonical_kappa.py -> hessian_lammps.py -> kappa_rpa.vineyard_prefactor) computes the
attempt frequency nu0 for each FCC(100) Ni hop. The code compiled and ran and emitted a
*plausible* number; only physical judgment caught that it was wrong.

Plotted from the real outputs in
apps/PyKMC_Analysis/Analysis/lattice_event_classification/family_prefactors.csv:

  subsurface 1NN:  with the full free region the Vineyard ratio gave nu0 = 167 THz (r=6)
                   and 470 THz (r=8) -- it grew as atoms were ADDED, the tell of a soft-mode
                   artefact (surface atoms ~0.88 A above the buried vacancy rattling at ~1 meV).
                   Auto-freezing them (free_min_z = mover_z + 0.88) -> nu0 = 27.6 THz.
  surface 1NN:     nu0 = 13.1 THz (validated; literature 5-15 THz, at the high end).

470 / 27.6 ~= 17x too high, and it never crashed. Literature bands (grey) are the human's
yardstick. The RPA recrossing kappa came back NaN (perturbation theory unreliable), so the
honest call was bare-Vineyard. Figure is generated by code -> reproducible artifact.

Run:  python3 assets/src/gen_htst_prefactor_catch.py
Out:  assets/htst_prefactor_catch.svg  (vector, offline)

Numbers pinned to real outputs (family_prefactors.csv, v6/v7, T = 500 K):
  surface_1NN_inplane:    nu0 = 13.1 THz, Ea = 0.629 eV, correction 1.31x
  subsurface_1NN_inplane: nu0 = 27.6 THz, Ea = 0.826 eV, correction 2.76x
  subsurface pre-fix:     167 THz (r=6), 470 THz (r=8), full free region
  literature: surface 1NN 5-15 THz; subsurface bulk-like 1NN 10-30 THz (HTST.md)
"""
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt

# ----- Queen's brand + Okabe-Ito (data colours are colourblind-safe, never red/gold) -----
Q_BLUE = "#002452"
Q_RED  = "#B90E31"
OI_BLUE = "#0072B2"   # Okabe-Ito blue       -> physics-validated value
OI_VERM = "#D55E00"   # Okabe-Ito vermillion -> the plausible-but-wrong value
INK = "#212121"
MUTED = "#58595B"
PANEL = "#F1F2F2"
BAND = "#D9DBDC"      # neutral grey -> literature band (not red/gold)

plt.rcParams.update({
    "font.family": "DejaVu Sans",
    "font.size": 13,
    "axes.edgecolor": MUTED,
    "axes.linewidth": 1.0,
    "svg.fonttype": "none",   # keep text as text in the SVG (smaller, selectable)
})

fig, ax = plt.subplots(figsize=(8.8, 4.5), dpi=200)
fig.patch.set_facecolor("white")
ax.set_facecolor("white")
ax.set_xscale("log")

# rows: one per motif
Y_SUB, Y_SURF = 1.0, 0.0

# literature bands (the human's yardstick)
ax.fill_betweenx([Y_SUB - 0.28, Y_SUB + 0.28], 10, 30, color=BAND, zorder=0)
ax.fill_betweenx([Y_SURF - 0.28, Y_SURF + 0.28], 5, 15, color=BAND, zorder=0)
ax.text(30, Y_SUB + 0.34, "literature 10-30 THz", color=MUTED, fontsize=10, ha="right", va="bottom")
ax.text(15, Y_SURF - 0.34, "literature 5-15 THz", color=MUTED, fontsize=10, ha="right", va="top")

# subsurface: the wrong cloud (grew with more atoms) -> arrow down to the validated value
ax.scatter([167, 470], [Y_SUB, Y_SUB], s=130, color=OI_VERM, zorder=3,
           edgecolor="white", linewidth=1.2)
ax.text(470, Y_SUB + 0.13, "470 THz  (r=8)", color=OI_VERM, fontsize=11,
        fontweight="bold", ha="center", va="bottom")
ax.text(167, Y_SUB + 0.13, "167 (r=6)", color=OI_VERM, fontsize=10, ha="center", va="bottom")
ax.annotate("", xy=(27.6, Y_SUB - 0.02), xytext=(430, Y_SUB - 0.02),
            arrowprops=dict(arrowstyle="->", color=Q_RED, lw=2.2,
                            connectionstyle="arc3,rad=0.0"))
ax.text(95, Y_SUB - 0.20, "auto-freeze the soft modes", color=Q_RED, fontsize=10.5,
        fontweight="bold", ha="center", va="top")
ax.scatter([27.6], [Y_SUB], s=160, color=OI_BLUE, zorder=4, edgecolor="white", linewidth=1.4)
ax.text(27.6, Y_SUB + 0.13, "27.6 THz", color=OI_BLUE, fontsize=11.5,
        fontweight="bold", ha="center", va="bottom")

# surface: a clean validated point
ax.scatter([13.1], [Y_SURF], s=160, color=OI_BLUE, zorder=4, edgecolor="white", linewidth=1.4)
ax.text(13.1, Y_SURF + 0.13, "13.1 THz", color=OI_BLUE, fontsize=11.5,
        fontweight="bold", ha="center", va="bottom")

ax.set_yticks([Y_SURF, Y_SUB])
ax.set_yticklabels(["surface 1NN\n(Ea 0.63 eV)", "subsurface 1NN\n(Ea 0.83 eV)"],
                   color=INK, fontsize=11.5)
ax.set_ylim(-0.7, 1.7)
ax.set_xlim(4, 700)
ax.set_xlabel(r"attempt frequency $\nu_0$  (THz, log scale)", color=INK, fontsize=13)
ax.set_title("An attempt frequency that compiled clean -- and was 17x too high",
             color=Q_BLUE, fontsize=14.5, fontweight="bold", pad=10)
ax.tick_params(colors=MUTED)
for s in ("top", "right", "left"):
    ax.spines[s].set_visible(False)

# legend by proxy
from matplotlib.lines import Line2D
handles = [
    Line2D([0], [0], marker="o", color="white", markerfacecolor=OI_VERM,
           markersize=11, label="AI build, compiled clean (wrong)"),
    Line2D([0], [0], marker="o", color="white", markerfacecolor=OI_BLUE,
           markersize=11, label="physics-validated"),
]
leg = ax.legend(handles=handles, loc="lower right", frameon=True, fontsize=11)
leg.get_frame().set_edgecolor(PANEL)
leg.get_frame().set_facecolor("white")

fig.text(0.012, -0.03,
         "Real outputs (family_prefactors.csv). nu0 GREW as atoms were added -- the tell of a "
         "soft-mode artefact, not physics. The recrossing kappa came back NaN, so the honest call was bare-Vineyard.",
         color=MUTED, fontsize=10, ha="left")

fig.tight_layout(rect=(0, 0.03, 1, 1))
out = __file__.rsplit("/assets/", 1)[0] + "/assets/htst_prefactor_catch.svg"
fig.savefig(out, format="svg", bbox_inches="tight", facecolor="white",
            metadata={"Date": None})

# Strip matplotlib's RDF <metadata> block: it carries remote namespace URLs
# (purl.org, creativecommons.org, matplotlib.org) that are never fetched but
# would trip the deck's offline "no remote URL" grep. Keep the deck 100% local.
import re
with open(out, "r", encoding="utf-8") as fh:
    svg = fh.read()
svg = re.sub(r"<metadata>.*?</metadata>\s*", "", svg, flags=re.DOTALL)
with open(out, "w", encoding="utf-8") as fh:
    fh.write(svg)
print("wrote", out)
print("subsurface: 470 THz (r=8) -> 27.6 THz validated ; ratio = %.1fx" % (470.0 / 27.6))
print("surface: 13.1 THz validated (literature 5-15 THz)")
