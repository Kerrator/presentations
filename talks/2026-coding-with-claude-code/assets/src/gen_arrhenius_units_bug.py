#!/usr/bin/env python3
"""Arrhenius figure for "Coding with Claude Code": the Hz -> ps^-1 units bug.

Same physical barrier (Ni(100) surface hop, dE ~= 0.63 eV, attempt frequency
nu0 ~= 3 THz), two rate curves:

  correct:  k = nu0[ps^-1] * exp(-dE / kB T)            (nu0 in per-picosecond)
  buggy:    k = nu0[Hz]    * exp(-dE / kB T) as if ps^-1 (the numeral 3e12 fed
            straight into a ps^-1 clock) -> every rate 1e12 too large

The two lines are parallel (identical slope, same dE); the bug is a pure 12-decade
vertical shift. Nothing crashes, the suite stays green, and a residence time
tau = 1/k is 1e12 too small. This figure is generated by code (no AI image
generation) so it is a reproducible, version-controlled artifact, which is the
deck's own thesis.

Run:  python3 assets/src/gen_arrhenius_units_bug.py
Out:  assets/arrhenius_units_bug.svg  (vector, offline)

Numbers pinned to the literature (see the deck's speaker notes):
  dE (EAM Ni(001) single-adatom hop) ~ 0.63 eV  (Mehl et al., PRB 60, 2106, 1999)
  nu0 (metal-surface adatom prefactor) ~ 1-10 THz; 3 THz used here as a midpoint
  1 THz = 1e12 Hz = 1 ps^-1   (1 ps = 1e-12 s)
"""
import numpy as np
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt

# ----- Queen's brand + Okabe-Ito (data colours are colourblind-safe, never red/gold) -----
Q_BLUE = "#002452"
Q_RED  = "#B90E31"
OI_BLUE = "#0072B2"   # Okabe-Ito blue   -> correct rate
OI_VERM = "#D55E00"   # Okabe-Ito vermillion -> buggy rate
INK = "#212121"
MUTED = "#58595B"
PANEL = "#F1F2F2"

plt.rcParams.update({
    "font.family": "DejaVu Sans",
    "font.size": 13,
    "axes.edgecolor": MUTED,
    "axes.linewidth": 1.0,
    "svg.fonttype": "none",   # keep text as text in the SVG (smaller, selectable)
})

# ----- physics -----
kB = 8.617333262e-5          # eV / K
dE = 0.63                    # eV, Ni(100) EAM hop barrier (Mehl 1999)
nu0_ps = 3.0                 # ps^-1  (3 THz, correct units)
nu0_buggy = nu0_ps * 1e12    # the Hz numeral (3e12) shoved into a ps^-1 clock

T = np.linspace(300.0, 1000.0, 200)
inv = 1000.0 / T             # 1000 / T  (1/K), conventional Arrhenius x-axis
k_correct = nu0_ps   * np.exp(-dE / (kB * T))   # ps^-1
k_buggy   = nu0_buggy * np.exp(-dE / (kB * T))  # ps^-1 (wrong by 1e12)

fig, ax = plt.subplots(figsize=(8.6, 4.6), dpi=200)
fig.patch.set_facecolor("white")
ax.set_facecolor("white")

ax.plot(inv, np.log10(k_buggy),   color=OI_VERM, lw=3.2, label="buggy: nu0 in Hz, read as ps$^{-1}$")
ax.plot(inv, np.log10(k_correct), color=OI_BLUE, lw=3.2, label="correct: nu0 in ps$^{-1}$")

# the 1e12 gap brace + label, drawn at a representative temperature
xg = inv[len(inv) // 2]
ylo = np.log10(k_correct[len(inv) // 2])
yhi = np.log10(k_buggy[len(inv) // 2])
ax.annotate("", xy=(xg, yhi), xytext=(xg, ylo),
            arrowprops=dict(arrowstyle="<->", color=Q_RED, lw=2.0))
ax.text(xg + 0.05, 0.5 * (ylo + yhi),
        r"$\times 10^{12}$" + "\nevery HTST rate,\nsilently",
        color=Q_RED, fontsize=13, fontweight="bold", va="center", ha="left")

# zero line (k = 1 ps^-1) for reference
ax.axhline(0.0, color=MUTED, lw=0.8, ls=(0, (3, 4)))

ax.set_xlabel("1000 / T   (K$^{-1}$)", color=INK, fontsize=13)
ax.set_ylabel(r"$\log_{10}\,k$   ($k$ in ps$^{-1}$)", color=INK, fontsize=13)
ax.set_title("Same barrier, two unit conventions: the units bug is a 12-decade shift",
             color=Q_BLUE, fontsize=14.5, fontweight="bold", pad=10)
ax.tick_params(colors=MUTED)
for s in ("top", "right"):
    ax.spines[s].set_visible(False)
leg = ax.legend(loc="upper right", frameon=True, fontsize=11.5)
leg.get_frame().set_edgecolor(PANEL)
leg.get_frame().set_facecolor("white")

# caption strip below the axes
fig.text(0.012, -0.02,
         "Ni(100) hop: dE ≈ 0.63 eV (Mehl 1999), nu0 ≈ 3 THz. "
         "1 THz = 1e12 Hz = 1 ps⁻¹. Nothing crashes; the suite is green; τ = 1/k is 1e12 too small.",
         color=MUTED, fontsize=10.5, ha="left")

fig.tight_layout(rect=(0, 0.02, 1, 1))
out = __file__.rsplit("/assets/", 1)[0] + "/assets/arrhenius_units_bug.svg"
fig.savefig(out, format="svg", bbox_inches="tight", facecolor="white",
            metadata={"Date": None})

# Strip matplotlib's RDF <metadata> block: it carries remote namespace URLs
# (purl.org, creativecommons.org, matplotlib.org) that are never fetched but
# would trip the deck's offline "no remote URL" grep. Keep the deck 100% local.
import re
with open(out, "r", encoding="utf-8") as fh:
    svg = fh.read()
svg = re.sub(r"<metadata>.*?</metadata>\s*", "", svg, flags=re.DOTALL)
with open(out, "w", encoding="utf-8") as fh:
    fh.write(svg)
print("wrote", out)
print("k_correct(500K) = %.3e ps^-1 ; k_buggy(500K) = %.3e ps^-1 ; ratio = %.1e"
      % (nu0_ps * np.exp(-dE / (kB * 500.0)),
         nu0_buggy * np.exp(-dE / (kB * 500.0)),
         (nu0_buggy / nu0_ps)))
